#include "libft.h"

char	**ft_flines(char **p, char const *s, char c)
{
	int i;
	int n;
	int h;

	n = 0;
	h = 0;
	i = 0;
	while (s[i] != '\0' && n < ft_qlines(s, c))
	{
		while (s[i] == c)
			i++;
		while (s[i] != c && s[i] != '\0')
		{
			p[n][h] = s[i];
			i++;
			h++;
		}
		p[n][h] = '\0';
		h = 0;
		n++;
	}
	p[n] = 0;
	return (p);
}
