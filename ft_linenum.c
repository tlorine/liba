int		ft_linenum(const char *string)
{
	int	i;
	int	nb;

	i = 0;
	nb = 0;
	while (string[i] >= '0' && string[i] <= '9')
	{
		nb = (nb * 10) + (string[i] - '0');
		i++;
	}
	return (nb);
}
