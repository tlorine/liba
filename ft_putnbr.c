#include "libft.h"

void	ft_putnbr(int nu)
{
	long long int	n;

	n = nu;
	if (n < 0)
	{
		ft_putchar('-');
		n = n - n - n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	ft_putchar(n % 10 + '0');
}
